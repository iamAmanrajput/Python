# ================= FOR LOOP =================
# Note:
# For loop ka use tab hota hai jab hume kisi sequence (list, string, range etc.)
# ke elements ko ek-ek karke iterate karna ho.

# Syntax:
# for variable in sequence:
#     code

# Example:
for i in range(1, 6):
    print("For Loop:", i)

# Output:
# For Loop: 1
# For Loop: 2
# For Loop: 3
# For Loop: 4
# For Loop: 5


# ================= WHILE LOOP =================
# Note:
# While loop tab use hota hai jab loop condition true hone tak chalana ho.

# Syntax:
# while condition:
#     code

# Example:
i = 1
while i <= 5:
    print("While Loop:", i)
    i += 1

# Output:
# While Loop: 1
# While Loop: 2
# While Loop: 3
# While Loop: 4
# While Loop: 5